    public void cargarPlatosEnTablas() {
        for (int i = tablaPrimeros.getRowCount() - 1; i >= 0; i--) {
            tablaPrimeros.removeRow(i);
        }
        for (int i = 0; i < platoSRV.listaPlatosPrincipales().size(); i++) {
            String datos[] = {platoSRV.listaPlatosPrincipales().get(i).getDescripcion(), platoSRV.listaPlatosPrincipales().get(i).getPrecio()};
            tablaPrimeros.addRow(datos);
        }
        //    platoPrincipaljTable.setModel(tablaPrimeros);
        for (int i = tablaSegundos.getRowCount() - 1; i >= 0; i--) {
            tablaSegundos.removeRow(i);
        }
        for (int i = 0; i < platoSRV.listaPlatosSecundarios().size(); i++) {
            String datos[] = {platoSRV.listaPlatosSecundarios().get(i).getDescripcion(), platoSRV.listaPlatosSecundarios().get(i).getPrecio()};
            tablaSegundos.addRow(datos);
        }
        // platoSecundariojTable.setModel(tablaSegundos);
        for (int i = tablaBebidas.getRowCount() - 1; i >= 0; i--) {
            tablaBebidas.removeRow(i);
        }
        Iterator ite3 = platoSRV.listaBebidas().iterator();
        while (ite3.hasNext()) {
            Plato plato = (Plato) ite3.next();
            String datos[] = {plato.getDescripcion(), plato.getPrecio()};
            tablaBebidas.addRow(datos);
        }
        //   bebidajTable.setModel(tablaBebidas);
        for (int i = tablaExtras.getRowCount() - 1; i >= 0; i--) {
            tablaExtras.removeRow(i);
        }
        Iterator ite4 = platoSRV.listaPostres().iterator();
        while (ite4.hasNext()) {
            Plato plato = (Plato) ite4.next();
            String datos[] = {plato.getDescripcion(), plato.getPrecio()};
            tablaExtras.addRow(datos);
        }
        //  extrajTable.setModel(tablaExtras);
    }

    public void cargarCombosEnTabla() {
        for (int i = tablaCombos.getRowCount() - 1; i >= 0; i--) {
            tablaCombos.removeRow(i);
        }
        for (int i = 0; i < comboSRV.listaCombos().size(); i++) {
            String descripcion = "";
            String principal = "";
            String segundo = "";
            String bebida = "";
            String postre = "";
            String precio = "";
            if (comboSRV.listaCombos().get(i) != null) {
                descripcion = comboSRV.listaCombos().get(i).getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPrincipal() != null) {
                principal = comboSRV.listaCombos().get(i).getPrincipal().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getSegundo() != null) {
                segundo = comboSRV.listaCombos().get(i).getSegundo().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getBebida() != null) {
                bebida = comboSRV.listaCombos().get(i).getBebida().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPostre() != null) {
                postre = comboSRV.listaCombos().get(i).getPostre().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPrecio() != null) {
                precio = comboSRV.listaCombos().get(i).getPrecio();
            }
            String datos[] = {descripcion, principal, segundo, bebida, postre, precio};
            tablaCombos.addRow(datos);
        }
        contadorCombo = comboSRV.numeroCombo();
        numeroComboTXT.setText(Integer.toString(contadorCombo));
        combosjTable.clearSelection();

        //   combosjTable.setModel(tablaCombos);
    }

