    private void modificarPlatoBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarPlatoBTNActionPerformed
        //habilitarBotones();
        if (filaSeleccionadaBebida == -1 && filaSeleccionadaExtra == -1 && filaSeleccionadaPrincipal == -1 && filaSeleccionadaSecundario == -1) {
            JOptionPane.showMessageDialog(null, "No seleciono fila");
        }
        if (filaSeleccionadaPrincipal >= 0) {
            String descripcion = principalTXT.getText();
            String precio = precioPrincipalTXT.getText();
            String datos[] = {descripcion, precio};
            for (int i = 0; i < tablaPrimeros.getColumnCount(); i++) {
                tablaPrimeros.setValueAt(datos[i], filaSeleccionadaPrincipal, i);
            }
            platoSRV.modificarPlatoPrincipal(platoSRV.listaPlatosPrincipales().get(filaSeleccionadaPrincipal), descripcion, precio);
            // this.platoPrincipaljTable.setModel(tablaPrimeros);
        }
        if (filaSeleccionadaSecundario >= 0) {
            String datos[] = {secundarioTXT.getText(), precioSecundarioTXT.getText()};
            for (int i = 0; i < tablaSegundos.getColumnCount(); i++) {
                tablaSegundos.setValueAt(datos[i], filaSeleccionadaSecundario, i);
            }
            //this.platoSecundariojTable.setModel(tablaSegundos);
        }
        if (filaSeleccionadaBebida >= 0) {
            String datos[] = {bebidaTXT.getText(), precioBebidaTXT.getText()};
            for (int i = 0; i < tablaBebidas.getColumnCount(); i++) {
                tablaBebidas.setValueAt(datos[i], filaSeleccionadaBebida, i);
            }
            //this.bebidajTable.setModel(tablaBebidas);
        }
        if (filaSeleccionadaExtra >= 0) {
            String datos[] = {extraTXT.getText(), precioExtraTXT.getText()};
            for (int i = 0; i < tablaExtras.getColumnCount(); i++) {
                tablaExtras.setValueAt(datos[i], filaSeleccionadaExtra, i);
            }
            //this.extrajTable.setModel(tablaExtras);
        }
        actualizarFormPlatos();
        habilitarBotonesFormPlatos();
    }//GEN-LAST:event_modificarPlatoBTNActionPerformed

