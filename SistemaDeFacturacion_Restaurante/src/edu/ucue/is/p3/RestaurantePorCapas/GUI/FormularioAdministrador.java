    private void eliminarPlatoBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarPlatoBTNActionPerformed

        if (filaSeleccionadaPrincipal == -1 && filaSeleccionadaSecundario == -1 && filaSeleccionadaBebida == -1 && filaSeleccionadaExtra == -1) {
            JOptionPane.showMessageDialog(null, "No seleciono fila");
        }
        if (filaSeleccionadaPrincipal >= 0) {
            platoSRV.eliminarPlatoPrincipal(platoSRV.listaPlatosPrincipales().get(filaSeleccionadaPrincipal));
            tablaPrimeros.removeRow(filaSeleccionadaPrincipal);
        }
        if (filaSeleccionadaSecundario >= 0) {
            platoSRV.eliminarPlatoSecundario(platoSRV.listaPlatosSecundarios().get(filaSeleccionadaSecundario));
            tablaSegundos.removeRow(filaSeleccionadaSecundario);
        }
        if (filaSeleccionadaBebida >= 0) {
            platoSRV.eliminarExtraBebida(platoSRV.listaBebidas().get(filaSeleccionadaBebida));
            tablaBebidas.removeRow(filaSeleccionadaBebida);
        }
        if (filaSeleccionadaExtra >= 0) {
            platoSRV.eliminarExtraPostre(platoSRV.listaPostres().get(filaSeleccionadaExtra));
            tablaExtras.removeRow(filaSeleccionadaExtra);

        }
        actualizarFormPlatos();
        habilitarBotonesFormPlatos();
    }//GEN-LAST:event_eliminarPlatoBTNActionPerformed
