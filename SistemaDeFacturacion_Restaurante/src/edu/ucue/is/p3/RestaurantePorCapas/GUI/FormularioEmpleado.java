    private void cargarTablasConDatosPedidos() {
        for (int i = combosjTable.getRowCount() - 1; i >= 0; i--) {
            tablaCombos.removeRow(i);
        }
        for (int i = 0; i < comboSRV.listaCombos().size(); i++) {
            String descripcion = "";
            String principal = "";
            String segundo = "";
            String bebida = "";
            String postre = "";
            String precio = "";
            if (comboSRV.listaCombos().get(i) != null) {
                descripcion = comboSRV.listaCombos().get(i).getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPrincipal() != null) {
                principal = comboSRV.listaCombos().get(i).getPrincipal().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getSegundo() != null) {
                segundo = comboSRV.listaCombos().get(i).getSegundo().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getBebida() != null) {
                bebida = comboSRV.listaCombos().get(i).getBebida().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPostre() != null) {
                postre = comboSRV.listaCombos().get(i).getPostre().getDescripcion();
            }
            if (comboSRV.listaCombos().get(i).getPrecio() != null) {
                precio = comboSRV.listaCombos().get(i).getPrecio();
            }
            String datos[] = {descripcion, principal, segundo, bebida, postre, precio};
            tablaCombos.addRow(datos);
        }
//        combosDeCliente.removeAll(combosDeCliente);

        combosjTable.clearSelection();
        for (int i = pedidosjTable.getRowCount() - 1; i >= 0; i--) {
            tablaPedidos.removeRow(i);
        }
        for (int i = 0; i < pedidoSRV.listaPedidos().size(); i++) {
            String nombre = pedidoSRV.listaPedidos().get(i).getCliente().getpNombre() + " " + pedidoSRV.listaPedidos().get(i).getCliente().getsNombre() + " " + pedidoSRV.listaPedidos().get(i).getCliente().getpApellido() + " " + pedidoSRV.listaPedidos().get(i).getCliente().getsApellido();
            String cedula = pedidoSRV.listaPedidos().get(i).getCliente().getCedula();
            String direccion = pedidoSRV.listaPedidos().get(i).getCliente().getDireccion();
            String telefono = pedidoSRV.listaPedidos().get(i).getCliente().getTelefono();
            double precio = 2.3;//pedidoSRV.listaPedidos().get(i).getFactura().getTotal();
            int mesa = pedidoSRV.listaPedidos().get(i).getMesa().getNumeroMesa();
            int pedidos = pedidoSRV.listaPedidos().size();
            String datos[] = {Integer.toString(i), nombre, cedula, direccion, telefono, Integer.toString(pedidos), Double.toString(precio), Integer.toString(mesa)};
            tablaPedidos.addRow(datos);
        }

        //   combosjTable.setModel(tablaCombos);
    }

