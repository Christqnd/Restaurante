    private void agregarPedidoBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarPedidoBTNActionPerformed
        try {
            String primNom = pNombreClienteTXT.getText();
            String segNom = sNombreClienteTXT.getText();
            String primApe = pApellidoClienteTXT.getText();
            String segApe = sApellidoClienteTXT.getText();
            String direc = direccionClienteTXT.getText();
            String cedula = cedulaClienteTXT.getText();
            String telefono = telefonoClienteTXT.getText();
            String numMesa = numeroMesaTXT.getText();
            Factura factura = new Factura(23.24, 23.34, 2.3, 004);
            Cliente cliente = new Cliente(primNom, segNom, primApe, segApe, cedula, telefono, direc, combosDeCliente, factura);
            pedidoSRV.insertarPedido(cliente, mesaSRV.buscarMesa(Integer.parseInt(numMesa)));
            limpiarDatosDeCliente();
            combosDeCliente.removeAll(combosDeCliente);
        } catch (AtributoRequeridoException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } catch (MesaNoDisponibleException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } finally {
            cargarTablasConDatosPedidos();
        }
    }//GEN-LAST:event_agregarPedidoBTNActionPerformed

